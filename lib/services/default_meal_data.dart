  // 랜덤 끼니 생성: 주식 1 + 국/찌개 1 + 반찬 3 = 총 5개
  // ─────────────────────────────────────────────────────────────
  static List<MealItem> _pickRandom(
      List<MealItem> mains, List<MealItem> soups, List<MealItem> sides) {
    final shuffledSides = List.of(sides)..shuffle(_rng);
    return [
      mains[_rng.nextInt(mains.length)],
      soups[_rng.nextInt(soups.length)],
      shuffledSides[0],
      shuffledSides[1],
      shuffledSides[2],
    ];
  }

  static List<MealItem> randomBreakfast() =>
      _pickRandom(_bMains, _bSoups, _bSides);

  static List<MealItem> randomLunch() =>
      _pickRandom(_lMains, _lSoups, _lSides);

  static List<MealItem> randomDinner() =>
      _pickRandom(_dMains, _dSoups, _dSides);

  /// 특정 끼니 랜덤 (refreshMeal 실패 시 폴백용)
  static List<MealItem> randomMeal(String type) {
    switch (type) {
      case 'breakfast': return randomBreakfast();
      case 'lunch':     return randomLunch();
      case 'dinner':    return randomDinner();
      default:          return randomBreakfast();
    }
  }

  /// 앱 시작 시 하루 식단 랜덤 생성
  static DayMeal defaultDay() => DayMeal(
    breakfast: randomBreakfast(),
    lunch:     randomLunch(),
    dinner:    randomDinner(),
  );
